package com.operators;

public class ArithmethicOperator {

	public static void main(String[] args) {

		int a = 21;
		int b = 10;

		int sum = a + b;
		System.out.println(sum);

		int sub = a - b;
		System.out.println(sub);

		int mul = a * b;
		System.out.println(mul);

		int div = a / b;
		System.out.println(div);
		
		int rem = a % b;
		System.out.println(rem);

	}

}

